/* ---- Runtime ---- */
let currentLang = localStorage.getItem('studyBuddy_lang') || 'en';

function t(key, vars) {
  let str = TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS['en']?.[key] ?? key;
  if (vars) Object.keys(vars).forEach(k => { str = str.replace('{' + k + '}', vars[k]); });
  return str;
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('studyBuddy_lang', lang);
  applyTranslations();
  document.documentElement.lang = lang;
  // Highlight the active button immediately — no refresh needed
  ['en','hi','gu'].forEach(l => {
    const btn = document.getElementById('lang-' + l);
    if (btn) btn.classList.toggle('lang-btn-active', l === lang);
  });
  // Also refresh any dynamic home stats text that uses t()
  if (typeof updateHomeStats === 'function') updateHomeStats();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key  = el.dataset.i18n;
    const attr = el.dataset.i18nAttr;   // e.g. data-i18n-attr="placeholder"
    const val  = t(key);
    if (attr) el.setAttribute(attr, val);
    else      el.textContent = val;
  });
}
